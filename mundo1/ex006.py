""" Faça um programa em Python que leia um número e mostre o seu dobro, o seu triplo e a raiz quadrada """
from math import sqrt
numero = int(input("Digite um número: "))
print(f"""O número é: {numero}
O seu dobro: {numero*2}
O seu triplo: {numero*3}
A raiz quadrada Pri: {sqrt(numero)}
A raiz quadrada Seg: {numero**(1/2)}
""")









